#!/usr/bin/env node
/**
 * gen-v1-status.mjs — regenerates V1_STATUS.md from V1_COMPLETION.yaml.
 * The dashboard is GENERATED; never hand-edit it. Run after any program change.
 * Dependency-free. Historical detail lives in workflow artifacts, not here.
 */
import { readFileSync, writeFileSync, existsSync } from 'node:fs'
import { resolve, dirname } from 'node:path'
import { fileURLToPath } from 'node:url'

const ROOT = resolve(dirname(fileURLToPath(import.meta.url)), '..')
const y = readFileSync(resolve(ROOT, 'V1_COMPLETION.yaml'), 'utf8')
const val = (k) => {
  const m = y.match(new RegExp(`^\\s*${k}:\\s*(.+)$`, 'm'))
  return m ? m[1].trim().replace(/\s+#.*$/, '').trim().replace(/^["']|["']$/g, '') : '—'
}
const phases = y.split(/\n\s*- name:\s*/).slice(1).map((b) => ({
  name: b.split(/\r?\n/)[0].trim().replace(/^["']|["']$/g, ''),
  status: (b.match(/^\s*status:\s*(.+)$/m) || [])[1]?.trim() ?? '—',
  evidence: (b.match(/^\s*evidence:\s*(.+)$/m) || [])[1]?.trim() ?? '—',
}))
const active = val('active_phase')
const mark = (s) => (s === 'PASS' ? '✅' : s === 'IN_PROGRESS' ? '▶️' : s === 'BLOCKED' ? '⛔' : s === 'FAILED' ? '❌' : '⬜')

const lines = []
lines.push('# V1_STATUS — GENERATED (do not edit)')
lines.push('')
lines.push('> Generated by `scripts/gen-v1-status.mjs` from `V1_COMPLETION.yaml`. Edits are overwritten.')
lines.push('> Rules enforced by `scripts/verify-v1-completion.mjs` + `.github/workflows/v1-program-guard.yml`.')
lines.push('')
lines.push(`- **Active phase:** \`${active}\``)
lines.push(`- **PR #119:** ${val('pr_119')} · **global enforce:** ${val('global_enforce')} · **new products:** ${val('new_products')}`)
lines.push(`- **Benchmark target:** ${val('benchmark_target')} (production ${val('production_as_benchmark_target')})`)
lines.push(`- **Handwriting in V1 auto-final:** ${val('handwriting_in_v1_auto_final')} · **positive Stripe delivery:** ${val('positive_stripe_delivery')}`)
lines.push('')
lines.push('## Phases')
lines.push('')
lines.push('| # | Phase | Status | Evidence |')
lines.push('|---|-------|--------|----------|')
phases.forEach((p, i) => {
  const ev = p.status === 'PASS' && existsSync(resolve(ROOT, p.evidence)) ? p.evidence : (p.status === 'PASS' ? `${p.evidence} (MISSING)` : '—')
  lines.push(`| ${i + 1} | ${p.name}${p.name === active ? ' ◀ active' : ''} | ${mark(p.status)} ${p.status} | ${ev} |`)
})
lines.push('')
lines.push('## Deployed truth (from V1_COMPLETION.yaml)')
lines.push(`- security hotfix #122: ${val('security_hotfix_pr_122')} · per-action auth: ${val('per_action_auth')} · Stripe recipient re-verify: ${val('stripe_recipient_reverification')}`)
lines.push(`- negative security smoke: ${val('negative_security_smoke')} · positive paid delivery: ${val('positive_paid_delivery')}`)
lines.push(`- browser PII: ${val('browser_pii')} · dedicated staging: ${val('dedicated_staging')}`)
lines.push('')

writeFileSync(resolve(ROOT, 'V1_STATUS.md'), lines.join('\n') + '\n')
console.log('[gen-v1-status] wrote V1_STATUS.md')
