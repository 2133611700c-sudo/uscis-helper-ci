#!/usr/bin/env node
/**
 * gen-corpus-manifest.mjs — V1_COMPLETION phase PRIVATE_DOCUMENT_REGISTRY.
 *
 * Reads the PRIVATE, GITIGNORED real-document folders and writes a PII-SAFE
 * manifest (sha256 + size + ext + keyword doctype guess + anon id ONLY). It
 * NEVER reads or emits document content, names, or filenames.
 *
 * Inputs (gitignored, may be absent in CI — that's fine, manifest is committed):
 *   qa-shots/private, test-fixtures/real-docs
 * Output (committed, PII-free):
 *   docs/document-coverage/PRIVATE_CORPUS_MANIFEST.safe.yaml
 *
 * Dependency-free. Deterministic (sorted by sha256). Run locally only.
 */
import { readdirSync, statSync, readFileSync, writeFileSync, existsSync, mkdirSync } from 'node:fs'
import { createHash } from 'node:crypto'
import { resolve, dirname, extname, basename } from 'node:path'
import { fileURLToPath } from 'node:url'

const ROOT = resolve(dirname(fileURLToPath(import.meta.url)), '..')
const DIRS = ['qa-shots/private', 'test-fixtures/real-docs']
const EXTS = new Set(['.jpg', '.jpeg', '.png', '.pdf', '.heic'])

const walk = (d) => {
  const out = []
  if (!existsSync(d)) return out
  for (const e of readdirSync(d)) {
    const p = resolve(d, e)
    const s = statSync(p)
    if (s.isDirectory()) out.push(...walk(p))
    else if (EXTS.has(extname(p).toLowerCase())) out.push(p)
  }
  return out
}

// keyword doctype guess from filename ONLY (filename is never emitted)
const guess = (f) => {
  const b = basename(f).toLowerCase()
  if (/passport|pasport|zagran|booklet/.test(b)) return 'passport_or_booklet'
  if (/ead/.test(b)) return 'ead'
  if (/i-?94/.test(b)) return 'i94'
  if (/birth|narodzh/.test(b)) return 'birth_certificate'
  if (/marriage|shlyub/.test(b)) return 'marriage_certificate'
  if (/divorce/.test(b)) return 'divorce_certificate'
  if (/military|viysk/.test(b)) return 'military_id'
  if (/i-?797/.test(b)) return 'i797'
  return 'unclassified'
}

const files = DIRS.flatMap((d) => walk(resolve(ROOT, d)))
const rows = files.map((f) => {
  const buf = readFileSync(f)
  return {
    sha256: createHash('sha256').update(buf).digest('hex'),
    bytes: buf.length,
    ext: extname(f).toLowerCase().slice(1),
    doctype_guess: guess(f),
  }
})
// dedupe by sha256, sort deterministically
const bySha = new Map()
for (const r of rows) if (!bySha.has(r.sha256)) bySha.set(r.sha256, r)
const uniq = [...bySha.values()].sort((a, b) => a.sha256.localeCompare(b.sha256))

const lines = []
lines.push('# PRIVATE_CORPUS_MANIFEST.safe.yaml — GENERATED, PII-FREE.')
lines.push('# Generated by scripts/gen-corpus-manifest.mjs from gitignored private folders.')
lines.push('# Contains ONLY sha256 + size + ext + keyword doctype guess. No names, no filenames,')
lines.push('# no document values. Originals are NEVER committed. Run locally; inputs are gitignored.')
lines.push(`schema_version: 1`)
lines.push(`total_unique_documents: ${uniq.length}`)
lines.push('documents:')
uniq.forEach((r, i) => {
  const id = `doc${String(i + 1).padStart(3, '0')}`
  lines.push(`  - { anon_id: ${id}, sha256: ${r.sha256}, bytes: ${r.bytes}, ext: ${r.ext}, doctype_guess: ${r.doctype_guess} }`)
})
const byType = {}
for (const r of uniq) byType[r.doctype_guess] = (byType[r.doctype_guess] || 0) + 1
lines.push('counts_by_doctype_guess:')
for (const [k, v] of Object.entries(byType).sort()) lines.push(`  ${k}: ${v}`)

const outDir = resolve(ROOT, 'docs/document-coverage')
if (!existsSync(outDir)) mkdirSync(outDir, { recursive: true })
writeFileSync(resolve(outDir, 'PRIVATE_CORPUS_MANIFEST.safe.yaml'), lines.join('\n') + '\n')
console.log(`[corpus-manifest] wrote ${uniq.length} unique docs (PII-free)`)
