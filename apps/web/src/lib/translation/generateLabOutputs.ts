function wrapPage(bodyContent: string): string {
  return `<!DOCTYPE html>
<html lang="en">
<head>
  <meta charset="UTF-8">
  <meta name="viewport" content="width=device-width, initial-scale=1.0">
  <style>${PAGE_STYLES}</style>
</head>
<body>${bodyContent}</body>
</html>`
}

