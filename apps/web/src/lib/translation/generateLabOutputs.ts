/**
 * Generate 4 downloadable HTML output files for the Translation Lab.
 *
 * File 1: translation-draft.html         — USCIS translation per 8 CFR 103.2(b)(3)
 * File 2: field-review-checklist.html    — AI extraction results + user corrections table
 * File 3: certification-statement.html   — standalone self-certification template to print and sign
 * File 4: filing-instructions.html       — step-by-step filing guide
 *
 * All are self-contained HTML (no external dependencies).
 * Browser: File → Print → Save as PDF works on all.
 */

import type { OCRResult, OCRField } from './mockOCR'
import type { SampleDocument } from './sampleDocuments'

// ---------------------------------------------------------------------------
// Helpers
// ---------------------------------------------------------------------------

function esc(s: string): string {
  return s
    .replace(/&/g, '&amp;')
    .replace(/</g, '&lt;')
    .replace(/>/g, '&gt;')
    .replace(/"/g, '&quot;')
    .replace(/'/g, '&#039;')
}

function today(): string {
  return new Date().toLocaleDateString('en-US', { year: 'numeric', month: 'long', day: 'numeric' })
}

function statusBadge(status: OCRField['status'], confidence: number): string {
  const colors: Record<OCRField['status'], string> = {
    pass:   '#16a34a',
    review: '#ca8a04',
    fail:   '#dc2626',
  }
  const labels: Record<OCRField['status'], string> = {
    pass:   'PASS',
    review: 'REVIEW',
    fail:   'FAIL',
  }
  return `<span style="background:${colors[status]};color:#fff;padding:2px 7px;border-radius:4px;font-size:10px;font-weight:bold">${labels[status]} ${confidence}%</span>`
}

function pageHeader(title: string, subtitle: string): string {
  return `
    <div style="border-bottom:2px solid #1D5CBB;padding-bottom:12px;margin-bottom:18px">
      <h1 style="font-size:15pt;font-weight:bold;text-align:center;text-transform:uppercase;letter-spacing:.05em;margin-bottom:6px">${esc(title)}</h1>
      <p style="font-size:10pt;text-align:center;color:#555">${esc(subtitle)}</p>
      <p style="font-size:9pt;text-align:center;color:#888;margin-top:4px">Generated by Messenginfo · ${today()} · messenginfo.com</p>
    </div>`
}

function draftBanner(): string {
  return `<div style="background:#fff8e1;border:1.5px solid #f59e0b;border-radius:4px;padding:8px 12px;margin-bottom:14px;font-size:10pt;color:#92400e;text-align:center">
    ⚠ DRAFT TEMPLATE — Review and sign before submitting to USCIS (8 CFR 103.2(b)(3))
  </div>`
}

