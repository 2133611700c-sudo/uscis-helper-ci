// ---------------------------------------------------------------------------
// Trigger browser download of a generated file
// ---------------------------------------------------------------------------

export function downloadLabFile(content: string, filename: string): void {
  const blob = new Blob([content], { type: 'text/html;charset=utf-8' })
  const url = URL.createObjectURL(blob)
  const a = document.createElement('a')
  a.href = url
  a.download = filename
  document.body.appendChild(a)
  a.click()
  document.body.removeChild(a)
  URL.revokeObjectURL(url)
}

// ---------------------------------------------------------------------------
// Generate all 4 files at once and return as a map
// ---------------------------------------------------------------------------

export interface LabOutputs {
  translationDraft: string
  reviewChecklist: string
  certificationStatement: string
  filingInstructions: string
}

export function generateAllLabOutputs(
  sample: SampleDocument,
  result: OCRResult,
  sourceLanguage: string,
): LabOutputs {
  return {
    translationDraft:       generateTranslationDraft(sample, result, sourceLanguage),
    reviewChecklist:        generateReviewChecklist(sample, result),
    certificationStatement: generateCertificationStatement(sample, sourceLanguage),
    filingInstructions:     generateFilingInstructions(sample, sourceLanguage),
  }
}
