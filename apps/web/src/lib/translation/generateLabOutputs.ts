// File 1: Translation Draft
// ---------------------------------------------------------------------------

export function generateTranslationDraft(
  sample: SampleDocument,
  result: OCRResult,
  sourceLanguage: string,
): string {
  const fieldMap = Object.fromEntries(result.fields.map((f) => [f.key, f.editedValue]))

  const fieldRows = result.fields
    .filter((f) => f.editedValue?.trim())
    .map((f) => `
      <tr>
        <td class="label">${esc(f.labelEn)}</td>
        <td class="value">${esc(f.editedValue)}</td>
      </tr>`)
    .join('')

  const body = `
    ${pageHeader(`Translation of ${sample.titleEn}`, `Original language: ${sourceLanguage} → English`)}
    ${draftBanner()}

    <div class="section">Document Information</div>
    <table>
      <tr><td class="label">Document Type</td><td class="value">${esc(sample.titleEn)}</td></tr>
      <tr><td class="label">Original Language</td><td class="value">${esc(sourceLanguage)}</td></tr>
      <tr><td class="label">Target Language</td><td class="value">English</td></tr>
      <tr><td class="label">Date of Translation</td><td class="value">${today()}</td></tr>
    </table>

    <div class="section">Document Fields — English Translation</div>
    <table>
      <thead>
        <tr><th>Field</th><th>Translation / Value</th></tr>
      </thead>
      <tbody>
        ${fieldRows || '<tr><td colspan="2" style="color:#999;text-align:center">No fields entered.</td></tr>'}
      </tbody>
    </table>

    <div class="cert-block">
      <h2>Translator Certification</h2>
      <p style="font-size:11pt;margin-bottom:12px;line-height:1.6">
        Pursuant to <strong>8 CFR 103.2(b)(3)</strong>, I certify that I am competent to translate
        from <strong>${esc(sourceLanguage)}</strong> into <strong>English</strong>, and that
        the above translation is complete and accurate to the best of my knowledge and belief.
      </p>
      <div class="sign-grid">
        <div><div class="sign-line">&nbsp;</div><div class="sign-label">Signature of Translator</div></div>
        <div><div class="sign-line">&nbsp;</div><div class="sign-label">Date</div></div>
        <div><div class="sign-line">&nbsp;</div><div class="sign-label">Printed Full Name</div></div>
        <div><div class="sign-line">&nbsp;</div><div class="sign-label">Phone / Email (optional)</div></div>
        <div style="grid-column:1/-1"><div class="sign-line">&nbsp;</div><div class="sign-label">Mailing Address</div></div>
        <div style="grid-column:1/-1"><div class="sign-line">&nbsp;</div><div class="sign-label">City, State, ZIP Code</div></div>
      </div>
    </div>

    <div class="footer">
      Prepared with Messenginfo · messenginfo.com · Draft template only — not a certified translation.<br>
      Translation requirements: 8 CFR 103.2(b)(3) · uscis.gov · Generated: ${today()}
    </div>`

  return wrapPage(body)
}

// ---------------------------------------------------------------------------
// File 2: Field Review Checklist
// ---------------------------------------------------------------------------

export function generateReviewChecklist(
  sample: SampleDocument,
  result: OCRResult,
): string {
  const rows = result.fields.map((f) => {
    const edited = f.editedValue !== f.aiValue
    const rowBg = f.status === 'pass' ? '' : f.status === 'review' ? 'background:#fffbeb' : 'background:#fff5f5'
    return `
      <tr style="${rowBg}">
        <td class="label">${esc(f.labelEn)}</td>
        <td style="font-size:10pt;color:#555">${esc(f.aiValue)}</td>
        <td style="font-size:10pt">${edited ? `<strong>${esc(f.editedValue)}</strong> <span style="color:#16a34a;font-size:9pt">✏ corrected</span>` : esc(f.editedValue)}</td>
        <td style="text-align:center">${statusBadge(f.status, f.confidence)}</td>
        ${f.note ? `<td style="font-size:9pt;color:#666;font-style:italic">${esc(f.note)}</td>` : '<td style="color:#aaa">—</td>'}
      </tr>`
  }).join('')

  const body = `
    ${pageHeader('Field Extraction Review Checklist', `${sample.titleEn} — AI Extraction Audit`)}

    <p style="font-size:11pt;margin-bottom:16px">
      This checklist shows each field extracted by AI, the original extracted value,
      the final confirmed value, confidence score, and any extraction notes.
      Fields marked <strong style="color:#ca8a04">REVIEW</strong> or
      <strong style="color:#dc2626">FAIL</strong> require manual verification.
    </p>

    <table>
      <thead>
        <tr>
          <th>Field</th>
          <th>AI Extracted</th>
          <th>Final Value</th>
          <th style="text-align:center">Confidence</th>
          <th>Notes</th>
        </tr>
      </thead>
      <tbody>${rows}</tbody>
    </table>

    <div style="background:#f5f5f5;border:1px solid #ddd;padding:12px 16px;border-radius:4px;font-size:10pt;margin-top:8px">
      <strong>Summary:</strong>
      &nbsp;&nbsp;✅ PASS: ${result.passCount}
      &nbsp;&nbsp;⚠️ REVIEW: ${result.reviewCount}
      &nbsp;&nbsp;❌ FAIL: ${result.failCount}
      &nbsp;&nbsp;|&nbsp;&nbsp;Overall AI confidence: <strong>${result.overallConfidence}%</strong>
      &nbsp;&nbsp;|&nbsp;&nbsp;Processing time: ${result.processingMs}ms
    </div>

    <div class="footer">
      Prepared with Messenginfo · messenginfo.com · ${today()}<br>
      Keep this checklist with your translation draft for your records.
    </div>`

  return wrapPage(body)
}

